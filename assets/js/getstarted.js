jQuery(document).ready(function($) {

    $("#btn-attorney").click(function(){
      $("#attorney").show();
      $("#financial").hide();
      $("#family").hide();
      if($("#btn-family").hasClass('gs-clicked')){
        $("#btn-family").removeClass('gs-clicked');
      };
      if($("#btn-financial").hasClass('gs-clicked')){
        $("#btn-financial").removeClass('gs-clicked');
      };
      if(!$("#btn-attorney").hasClass('gs-clicked')){
        $("#btn-attorney").addClass('gs-clicked');
      };
    });
    $("#btn-family").click(function(){
      $("#family").show();
      $("#attorney").hide();
      $("#financial").hide();
      if($("#btn-attorney").hasClass('gs-clicked')){
        $("#btn-attorney").removeClass('gs-clicked');
      };
      if($("#btn-financial").hasClass('gs-clicked')){
        $("#btn-financial").removeClass('gs-clicked');
      };
      if(!$("#btn-family").hasClass('gs-clicked')){
        $("#btn-family").addClass('gs-clicked');
      };
    });
    $("#btn-financial").click(function(){
      $("#financial").show();
      $("#family").hide();
      $("#attorney").hide();
      if($("#btn-attorney").hasClass('gs-clicked')){
        $("#btn-attorney").removeClass('gs-clicked');
      };
      if($("#btn-family").hasClass('gs-clicked')){
        $("#btn-family").removeClass('gs-clicked');
      };
      if(!$("#btn-financial").hasClass('gs-clicked')){
        $("#btn-financial").addClass('gs-clicked');
      };
    });
  
    $('#btn-attorney').trigger('click');
    $('#family').hide();
    $('#financial').hide();
  
  });